#include <stdlib.h>

void	ft_free(void *p)
{
	if (p != NULL)
		free(p);
}

void	ft_free2(void **p)
{
	int	idx;

	idx = 0;
	while (p[idx])
	{
		free(p[idx]);
		idx++;
	}
	free(p);
}
